package engine.graphics;

public class Glyph {
    public float x, y, w, h, xo, yo, xadvance;

    public Glyph(float x, float y, float w, float h, float xo, float yo, float xadvance) {
        this.x = x;
        this.y = y;
        this.w = w;
        this.h = h;
        this.xo = xo;
        this.yo = yo;
        this.xadvance = xadvance;
    }


}
